#!/usr/bin/env node
/* eslint-disable no-console */
const fs = require('fs');
const path = require('path');

const rootDir = path.resolve(__dirname, '..', '..');
const envPath = path.join(rootDir, '.env');
const outputPath = path.join(rootDir, '..', '..', 'dev', 'paperless', 'docker-compose.env');

function parseEnvFile(contents) {
  const env = {};
  const lines = contents.split(/\r?\n/);
  for (const line of lines) {
    if (!line || line.trim().startsWith('#')) continue;
    const idx = line.indexOf('=');
    if (idx === -1) continue;
    const key = line.slice(0, idx).trim();
    let value = line.slice(idx + 1).trim();
    if ((value.startsWith('"') && value.endsWith('"')) || (value.startsWith("'") && value.endsWith("'"))) {
      value = value.slice(1, -1);
    }
    env[key] = value;
  }
  return env;
}

function parseDatabaseUrl(urlString) {
  if (!urlString) return null;
  try {
    const url = new URL(urlString);
    return {
      host: url.hostname,
      port: url.port || '5432',
      dbname: url.pathname.replace(/^\//, ''),
      user: decodeURIComponent(url.username || ''),
      pass: decodeURIComponent(url.password || ''),
      sslmode: url.searchParams.get('sslmode') || '',
    };
  } catch (err) {
    return null;
  }
}

if (!fs.existsSync(envPath)) {
  console.error('Missing .env at repo root. Copy .env.example to .env and fill values.');
  process.exit(1);
}

const envRaw = fs.readFileSync(envPath, 'utf8');
const env = parseEnvFile(envRaw);

const dbUrl =
  env.PAPERLESS_DATABASE_URL ||
  env.DATABASE_URI_PAPERLESS ||
  env.DATABASE_URI ||
  env.DATABASE_URL;
const parsedDb = parseDatabaseUrl(dbUrl);

const paperlessDomain = env.PAPERLESS_DOMAIN || 'paperless.localhost';

const resolved = {
  PAPERLESS_SECRET_KEY: env.PAPERLESS_SECRET_KEY || 'CHANGE_ME',
  PAPERLESS_URL: env.PAPERLESS_URL || `http://${paperlessDomain}`,
  PAPERLESS_TIME_ZONE: env.PAPERLESS_TIME_ZONE || 'UTC',
  PAPERLESS_OCR_LANGUAGE: env.PAPERLESS_OCR_LANGUAGE || 'eng',
  PAPERLESS_CONSUMER_POLLING: env.PAPERLESS_CONSUMER_POLLING || '60',
  PAPERLESS_CONSUMER_POLLING_DELAY: env.PAPERLESS_CONSUMER_POLLING_DELAY || '5',
  PAPERLESS_ADMIN_USER: env.PAPERLESS_ADMIN_USER || 'admin',
  PAPERLESS_ADMIN_PASSWORD: env.PAPERLESS_ADMIN_PASSWORD || 'admin',
  PAPERLESS_REDIS: env.PAPERLESS_REDIS || `redis://:${env.REDIS_PASSWORD || 'redis123'}@redis:6379`,
  PAPERLESS_DBHOST: env.PAPERLESS_DBHOST || (parsedDb && parsedDb.host) || '',
  PAPERLESS_DBPORT: env.PAPERLESS_DBPORT || (parsedDb && parsedDb.port) || '5432',
  PAPERLESS_DBNAME: env.PAPERLESS_DBNAME || (parsedDb && parsedDb.dbname) || '',
  PAPERLESS_DBUSER: env.PAPERLESS_DBUSER || (parsedDb && parsedDb.user) || '',
  PAPERLESS_DBPASS: env.PAPERLESS_DBPASS || (parsedDb && parsedDb.pass) || '',
  PAPERLESS_DBSSLMODE: env.PAPERLESS_DBSSLMODE || (parsedDb && parsedDb.sslmode) || '',
  CLOUDFLARED_TOKEN: env.CLOUDFLARED_TOKEN || '',
};

const lines = [
  '# Generated from .env by infra/scripts/sync-paperless-env.js',
  '# Do not edit this file directly. Edit .env and re-run make sync-env.',
  '',
  `PAPERLESS_SECRET_KEY=${resolved.PAPERLESS_SECRET_KEY}`,
  `PAPERLESS_URL=${resolved.PAPERLESS_URL}`,
  `PAPERLESS_TIME_ZONE=${resolved.PAPERLESS_TIME_ZONE}`,
  `PAPERLESS_OCR_LANGUAGE=${resolved.PAPERLESS_OCR_LANGUAGE}`,
  `PAPERLESS_CONSUMER_POLLING=${resolved.PAPERLESS_CONSUMER_POLLING}`,
  `PAPERLESS_CONSUMER_POLLING_DELAY=${resolved.PAPERLESS_CONSUMER_POLLING_DELAY}`,
  `PAPERLESS_ADMIN_USER=${resolved.PAPERLESS_ADMIN_USER}`,
  `PAPERLESS_ADMIN_PASSWORD=${resolved.PAPERLESS_ADMIN_PASSWORD}`,
  `PAPERLESS_REDIS=${resolved.PAPERLESS_REDIS}`,
  `PAPERLESS_DBHOST=${resolved.PAPERLESS_DBHOST}`,
  `PAPERLESS_DBPORT=${resolved.PAPERLESS_DBPORT}`,
  `PAPERLESS_DBNAME=${resolved.PAPERLESS_DBNAME}`,
  `PAPERLESS_DBUSER=${resolved.PAPERLESS_DBUSER}`,
  `PAPERLESS_DBPASS=${resolved.PAPERLESS_DBPASS}`,
];

if (resolved.PAPERLESS_DBSSLMODE) {
  lines.push(`PAPERLESS_DBSSLMODE=${resolved.PAPERLESS_DBSSLMODE}`);
}

lines.push(`CLOUDFLARED_TOKEN=${resolved.CLOUDFLARED_TOKEN}`);
lines.push('');

fs.writeFileSync(outputPath, lines.join('\n'));
console.log(`Wrote ${outputPath}`);
